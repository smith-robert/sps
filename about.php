      <h3>Chapter of the Society of Physics Students at the
        University of Central Florida (SPS)</h3>
        <h3><small>Constitution Guidelines and Bylaws</small></h3>
        <h5 id="art1"> ARTICLE I NAME </h5> 
        <p>The name of this organization shall be “Chapter of the Society of Physics Students at the University of Central Florida.” </p>

        <h5 id="art2"> ARTICLE II PREAMBLE </h5>
        <p>The purpose of this Chapter shall be the advancement and diffusion of knowledge of the science of physics and the encouragement of student interest in physics throughout the academic and local communities. </p>

        <h5 id="art3"> ARTICLE III OFFICERS </h5>
        <p>The officers of the Chapter shall be a President, a Vice President, a Secretary, Treasurer, Netadmin, and a Historian; succession shall be in accordance with this order. These officers shall perform the duties prescribed by these bylaws and by the parliamentary authority adopted by this Chapter. In order to hold a leadership position in this organization members shall be required to meet the minimum leadership requirements as stated in the Golden Rule. The current minimum requirements to have a leadership position are: </p>
        <ol>
          <li>During fall and spring semester, be currently enrolled as a student activity fee-paying half-time student (currently defined as at least six (6) credit hours as an undergraduate degree-seeking student or a postbaccalaureate student, or at least five (5) credit hours in a graduate degree-seeking program). During summer session, be currently enrolled for at least three (3) credit hours in at least one of the terms. </li>
          <li>Have an minimum overall GPA of 2.5 for all hours earned while in attendance at UCF. </li>
          <li>Maintain satisfactory academic progress as defined in the current Undergraduate Catalog and the current Graduate Catalog of UCF, as appropriate. </li>
          <li> Be in good academic and disciplinary standing with the University, defined as not being on academic or disciplinary probation. </li>
          <li>Be free of any holds on university records.</li>
        </ol>

        <p>In addition the officers must be in good standing with the club, as outlined in Article VIII. Students who do not meet these minimum requirements may submit a written appeal to the Eligibility Appeals Board through the office of Student Activities within 2 weeks from the first day of class each semester. The offices that may be voted on are as follows: </p>

        <p>In addition the officers must be in good standing with the club, as outlined in Article VIII. Students who do not meet these minimum requirements may submit a written appeal to the Eligibility Appeals Board through the office of Student Activities within 2 weeks from the first day of class each semester. The offices that may be voted on are as follows:</p>
        <ul class='disc'>
          <li>President Conducts the SPS meetings and plans the order of conversation; promotes, plans, and coordinates chapter activities; keeps the chapters bylaws up to date; represents the chapter’s interests when required; has signature authority over club funds. </li>
          <li>Vice President Assists the president and fills in for the president in his/her absence; oversees refreshments for guest lectures, SPS meetings, and applicable activities; coordinates fund raising. This office is optional and may be deemed unnecessary by a majority vote. </li>
          <li> Secretary Keeps an attendance list and records minutes of the meetings; updates and keeps a current roster of SPS members; records names, phone numbers, and e mail addresses of all new members; announces meetings by e mail and by posting notice in the appropriate areas (SPS bulletin board, SPS web page, and departmental bulletin board) at least two days in advance of regular meetings and as soon as possible for special meetings; updates the calendar of events; responsible for meeting, activity, and advertisement fliers; assists chapter advisor with paper work for national office.</li>
          <li>Treasurer Keeps track of the chapter’s finances and earnings from fundraising activities; deposits and withdraws funds with president’s authorization and signature; keeps a ledger of petty cash expenses and earnings.</li>
          <li> Netadmin Maintains all SPS controlled computer equipment and assists secretary with maintenance of web related resources, documents, and other asigned duties delegated by President and Vice President. </li>
          <li> Historian Maintains official record of the activities and events of the Society of Physics Students in conjunction with the President, Vice President, and Secretary. This office is exempt from office that falls upon the secretary. </li>
        </ul>

        <p></p>
        <h5 id="art4">ARTICLE IV SELECTION OF OFFICERS</h5>
        <p>Officers, excluding the presidential office, shall be elected at the first official meeting in April of each year. At this time theVice President will become the President-Elect. Nominations shall be by open nomination at the first official meetingin March. Nominees must meet the requirements to hold a leadership position as stated in Article III. The elections shall be by closed ballot with the ballots being counted and the result being announced by the president. The faculty advisor shall monitor this process and ensure the count is correct. Each position will be voted on separately with the candidates being absent during the vote. Only members in good standing as outlined in Article VIII and present at the election may vote in the elections. Additionally, no candidate may be elected who will graduate before completing a least one half year in office. Officers are elected by a majority vote excluding the offices of netadmin and historian The excluded offices shall be appointed by the a majority vote of the elected officers and shall have a voice at officer meetings but no vote. No person may hold more than one office. No person may hold the same office for more than 1½ years. Officers elect shall assume their offices at the next official meeting following the election and shall hold office for one year or until their successors are duly elected. </p>

        <h5 id="art5">ARTICLE V REMOVAL OF OFFICERS </h5>
        <p>Officers no longer wishing to serve on the board must submit their resignation to the President at least two (2) weeks in advance. Prior to the officer’s final day he/she shall provide all documents relating to the organization and brief his/her replacement of current projects in his/her care. Officers may be removed from office by a 2/3 vote of members who are in good standing as outlined in Article VIII. This vote may be brought by any member in good standing. The election must be announced at the preceding official meeting. </p>

        <h5 id="art6"> ARTICLE IV VOTING POWERS OF OFFICERS </h5>
        <p>Officers may cast votes as regular members with the exception of the president who will only cast a vote when there is a tie. </p>

        <h5 id="art7"> ARTICLE VII VACANCIES </h5>
        <p>If there is a vacancy of office an election will be held at the next official meeting following, the outline of Article IV concerning voting procedure. The newly elected officer shall serve out the remainder of the term.</p>

        <h5 id="art8"> ARTICLE VIII MEMBERSHIP </h5>
        <dl>
          <dt><h6 id="art8-sect1">Section 1: Society of Physics Students Membership</h6></dt>
          <dd>Membership is limited to all students who have paid fees and are enrolled with the University of Central Florida. No discrimination shall be made on the basis of gender, race, color, creed, age, religion, disability, national origin, marital status, sexual orientation/preference, parental status or veteran’s status. No hazing or discrimination will be used as a condition of membership in this organization. Membership shall be open to all those individuals interested in physics.
            <dl>
              <dt><h6 id="clause1"> Clause 1</h6></dt>
              <dd> For graduate students in physics or related fields, all members elect either:
                <ul class="disc">
                  <li> Shall meet the requirements for undergraduate members elect, based upon their undergraduate records or, </li>
                  <li>Shall have completed at least one year of graduate work , meeting departmental standards for satisfactory progress towards an advanced degree. </li>
                </ul>
              </dd>
              <td><h6 id="clause2"> Clause 2</h6></dt>
                <dd>
                  For faculty members, research staff members, and UCF Alumni, who are in physics or related fields affiliated with UCF, election to affiliate membership shall be based upon their professional attainments and voted on during an official meeting. UCF faculty, staff, alumni, Central Florida Research Park employees, as well as any person without limitation with respect to age or occupation, interested in participating in the Society of Physics Students University of Central Florida but is not affiliated with the University of Central Florida may be non-voting members with voice but will not count towards quorum. 
                </dd>
              </dl>
            </dd>
            <dt><h6 id="art8-sect2"> Section 2: Membership Detail </h6></dt>
            <dd>
              <ul class="disc">
                <li>Membership may be obtained throughout the year. </li>
                <li> Members remain in good standing by keeping dues paid to the National Society and to the local chapter if a due is required. Members must also attend 75% of official meetings per semester. If a member attends fewer than 75% of the meetings of the previous semester, excluding the summer semester and a new member’s first semester, the member shall be placed on “inactive status” and the member shall not count in the total from which the 2/3 attendance required in Article X to hold an official meeting is calculated. A member may be taken off “inactive status”, and reinstated as a count towards quorum, the semester following a semester during which the member attended 75% of the scheduled meetings.</li>
                <li>Membership entitles members voting rights on anything brought before the chapter. Members may also attend chapter sponsored events. </li>
                <li>Membership may be revoked if a member does not keep up dues or fails to meet the requirements set forth in Article VIII Section 1. </li>
                <li>) Members must meet requirements for membership within four weeks of applying for membership to be in good standing. They may not vote in officer elections until they are in good standing. If the member has not complied within the four week period, they may not vote in any future club meetings nor attend any chapter functions. </li>
                <li>  Members who drop out of good standing have four weeks to rectify the situation. They are subject to the same conditions as new members as stated in (e). </li>
              </ul>
            </dl>

            <h5 id="art9"> ARTICLE IX: FINANCES </h5>
            <p> Local chapter dues are collected by the Treasurer. The option to charge dues and how much to charge will be voted on during the first official meeting of the newly elected officers. If local dues are taken, every member, except faculty/staff, are expected to pay. Signature authority will be passed from the outgoing president to the newly elected president during the period between the election and the next official meeting. The treasurer will also pass all necessary information to the newly elected treasurer during this period. The president and treasurer are responsible for allocating funds for activities, which have been voted on by the chapter. Both the president and treasurer must authorize a withdrawal of funds. If one of these is absent, the advisor may substitute. Once the president and treasurer have formulated a budget, it will be presented to the chapter at the next official meeting. In the event of the dissolution of the group the physics department shall take over any remaining funds. </p>

            <h5 id="art10"> ARTICLE X: MEETINGS </h5>
            <p>Regular meetings shall be held monthly as determined by the officers. The schedule of meetings shall be set forth for a semester at least two months before the beginning of that semester. Special meetings may be called by the president or vice president. These special meetings must be announced and posted as soon as possible. There shall be at least three meetings per semester. The summer semester shall be exempt from meetings unless otherwise decided by a majority chapter vote. An official meeting must have 2/3 of current members in good standing present and at least two officers present ( quorum ). Succession of order will follow as outlined in Article III. </p>

            <h5 id="art11"> ARTICLE XI: ADVISOR </h5>
            <p>The chapter advisor shall be a faculty member appointed by the Chairman of the Physics Department, after consultation with the chapter officers. The length of his/her term will be unconstrained as long as he/she stays a member of the faculty. The chapter advisor shall provide liaison among the chapter, the Physics Department and the national office of the SPS. The chapter advisor will supply counseling to the chapter, furnish continuity of experience in chapter operations, and provide long range perspective to insure departmental interest in and support of the SPS The chapter advisor shall assist the secretary in furnishing the national office with the required data and shall monitor finance allocations. The advisor may not vote in the chapter.</p>

            <h5 id="art12"> ARTICLE XII: REMOVAL OF ADVISOR </h5>
            <p> The advisor will be notified in writing of the possible removal at least 72 hours prior to the vote and will be allowed to address the organization in order to relate to members any relevant defense prior to the voting for removal. Upon a 2/3 majority vote of eligible members the advisor will be remove from his/her duties. A new advisor will be appointed by the Chairman of the Physics Department within 14 calendar days, after consulation with the chapter officers. </p>

            <h5 id='art13'> ARTICLE XIII: EXTERNAL AFFAIRS<h5>
            <p>This is a local chapter of the National Society of Physics Students. Items not covered in this constitution shall be governed by the National Society’s constitution where it does not conflict with the local chapter’s.  </p>

            <h5 id='art14'> ARTICLE XIV: PUBLICATION </h5>
            <p>This local chapter shall forward an annual report to the national chapter as required. A budget shall be drafted to be presented to the SGA for funding. This budget shall be drafted two months before the deadline for SGA funding and voted upon by the chapter. All publications of the organization must comply with the Golden Rule “Advertising and Signs” section, Student Organization Guidelines “Advertising” section, and the University Identity and Standards Manual. </p>

            <h5 id="art15"> ARTICLE XV: AMENDMENTS </h5>
            <p>These articles may be amended by a 2/3 vote of members in good standing, provided that the amendment shall have been proposed at least one meeting previous to the time of vote. Such amendments shall conform to the regulations established by the national constitution of the SPS or agree with the spirit set forth by the same. </p>

